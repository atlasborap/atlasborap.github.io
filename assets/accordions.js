var acc = document.getElementsByClassName("accordion-icerik-cisco-catalyst-9800-wireless-controller-konfigurasyon-modeli");
var i;

for (i = 0; i < acc.length; i++) {
  acc[i].addEventListener("click", function () {
    this.classList.toggle("active");

    var panel = this.nextElementSibling;
    if (panel.style.display === "block") {
      panel.style.display = "none";
      document.getElementById("icerik-accordion-cisco-catalyst-9800-wireless-controller-konfigurasyon-modeli").innerHTML = "+ İçerik"
    } else {
      panel.style.display = "block";
      document.getElementById("icerik-accordion-cisco-catalyst-9800-wireless-controller-konfigurasyon-modeli").innerHTML = "- İçerik"
    }
  });
}

